
public class Car {

  private String color;//private class
  private String make;//private class

  public String getColor() {//get method from private
    return color;
  }

  public void setColor(String newColor) {//set method
    color = newColor;
  }
  public String getMake() {//get method from private
     return make;
  }
  public void setMake(String newMake) {//set method
    make = newMake;
  }
}
